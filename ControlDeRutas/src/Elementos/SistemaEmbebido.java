public class SistemaEmbebido {
        private String placa;
        private int tMuestras;

    /**
     * @return the placa
     */
    public String getPlaca() {
        return placa;
    }

    /**
     * @param placa the placa to set
     */
    public void setPlaca(String placa) {
        this.placa = placa;
    }

    /**
     * @return the tMuestras
     */
    public int gettMuestras() {
        return tMuestras;
    }

    /**
     * @param tMuestras the tMuestras to set
     */
    public void settMuestras(int tMuestras) {
        this.tMuestras = tMuestras;
    }
        
}
