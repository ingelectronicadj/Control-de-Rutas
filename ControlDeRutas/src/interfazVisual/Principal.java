    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Archivo archivo = new Archivo();
        FormateadorDatos formater = new FormateadorDatos();

        archivo.setDatos(formater.trama2KML(SerialRX.getText()));

        GestorAlmacenamiento gestorA = new GestorAlmacenamiento();
        gestorA.setArchivo(archivo);
        String ruta = gestorA.guardarKML();
        
        if (ruta != null & !SerialRX.getText().equals("")) {
            Runtime obj = Runtime.getRuntime();
            try {
                obj.exec("google-earth " + ruta + ".kml");
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, "Imposible acceder a Google Earth - " + ruta);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

