    private void ConnectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConnectionActionPerformed
        if (!connected) {
            try {
                serialPort = new SerialPort(Puertos.getSelectedItem().toString());
                serialPort.openPort();
                serialPort.setParams(Integer.parseInt(Rates.getSelectedItem().toString()), 8, 1, 0);
                serialPort.addEventListener(new SerialPortReader());
                Puertos.setEnabled(connected);
                Rates.setEnabled(connected);
                UpdatePorts.setEnabled(connected);
                Connection.setText("Desconectar");
                connected = !connected;
            } catch (NumberFormatException | SerialPortException | NullPointerException ex) {
                JOptionPane.showMessageDialog(null, "Error en los datos del puerto de comunicaciones");
                System.out.println(ex);
            }
        } else {
            try {
                serialPort.closePort();
                Puertos.setEnabled(connected);
                UpdatePorts.setEnabled(connected);
                Rates.setEnabled(connected);
                Connection.setText("Conectar");
                connected = !connected;
            } catch (Exception ex) {
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_ConnectionActionPerformed

    private void updatePorts() {
        String[] portNames = SerialPortList.getPortNames();
        Puertos.removeAllItems();
        for (String portName : portNames) {
            Puertos.addItem(portName);
        }
    }

    static class SerialPortReader implements SerialPortEventListener {
        private String str = "";
        @Override
        public void serialEvent(SerialPortEvent event) {
            if (event.isRXCHAR()) {
                try {
                    String read = serialPort.readString();
                    if (read.contains("\n")) {
                        str += read;
                        System.err.print( str);
                        switch (estado) {
                            case 0:
                                break;
                            case 1:
                                SerialRX.append(str);
                                break;
                            case 2:
                                StringTokenizer stk = new StringTokenizer(str, ",");
                                if (stk.countTokens() > 1) {
                                    try {
                                        embebido.settMuestras(Integer.parseInt(stk.nextToken().substring(1)));
                                        embebido.setPlaca(stk.nextToken());
                                        Tiempo.setValue(embebido.gettMuestras());
                                        Placa.setText(embebido.getPlaca().trim());
                                    } catch (NumberFormatException e) {
                                        Tiempo.setValue(" ");
                                        Placa.setText(" ");
                                    }
                                    estado = 0;
                                }
                                break;
                            default:
                                throw new AssertionError();
                        }
                        str = "";
                    } else {
                        str += read;
                    }
                } catch (SerialPortException ex) {
                    System.err.println(ex);
                }
            }
        }

    }

